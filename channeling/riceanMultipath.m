function y=riceanMultipath(x,fs,fd)
fs_a=200e3;
ricianChan = comm.RicianChannel(...
               'SampleRate',fs,...
               'PathDelays',[0.0 0.05 0.6 1.6]/fs_a,...
                'AveragePathGains',[0 -2 -10 -3],...
                'KFactor',4,...
                'DirectPathDopplerShift',5.0,...
                'DirectPathInitialPhase',0.5,...
                'MaximumDopplerShift',fd,...
                'DopplerSpectrum',doppler('Bell', 8),...
                'RandomStream','mt19937ar with seed', ...
                'Seed',73, ...
                'PathGainsOutputPort',true);
    %ricianChan.Visualization = 'Impulse response'; 
    %ricianChan.Visualization = 'Frequency response';

    y=ricianChan(x);
    %pause();
    

end
